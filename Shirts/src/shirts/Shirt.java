package shirts;

public class Shirt {

    private int shirtID;
    private String shirtName;
    private String shirtBrand;
    private int price;

    public int getShirtID() { return shirtID; }

    public void setShirtID(int shirtID) {this.shirtID = shirtID; }

    public String getShirtName() { return shirtName; }

    public void setShirtName(String shirtName) {this.shirtName = shirtName; }

    public String getShirtBrand() { return shirtBrand; }

    public void setShirtBrand(String shirtBrand) {this.shirtBrand = shirtBrand; }

    public int getPrice() { return price; }

    public void setPrice(int price) {this.price = price; }

}
